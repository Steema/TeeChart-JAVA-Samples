    private void setButtonsListeners() {
        ActionListener btnListener = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (e.getSource() == btnStockMonitoring) {
                    showChart(new StockMonitoring());
                } else if (e.getSource() == btnWebAnalytics) {
                    showChart(new WebAnalytics());
                } else if (e.getSource() == btnSalesFigures) {
                    showChart(new SalesFigures());
                } else if (e.getSource() == btnStackedBars) {
                    showChart(new StackedBars());
                } else if (e.getSource() == btnServerStatus) {
                    showChart(new ServerStatus());
                } else if (e.getSource() == btnVegetationGrowth) {
                    showChart(new VegetationGrowth());
                } else if (e.getSource() == btnSalesFiguresPie) {
                    showChart(new PieSalesFigures());
                } else if (e.getSource() == btnMultiplePies) {
                    showChart(new MultiPies());
                } else if (e.getSource() == btnDonut) {
                    showChart(new DonutChart());
                } else if (e.getSource() == btnMultiDonut) {
                    showChart(new MultiDonut());
                } else if (e.getSource() == btnProductShipments) {
                    showChart(new ProductShipments());
                } else if (e.getSource() == btnProjectPlanner) {
                    showChart(new ProjectPlanner());
                } else if (e.getSource() == btnPopularCodeLanguages) {
                    showChart(new PopularCodeLanguages());
                } else if (e.getSource() == btnScroller) {
                    showChart(new ScrollerChart());
                }

                setButtonsLocations();
            }
        };

        pnlTitle.addMouseListener(new MouseAdapter() {

            @Override
            public void mouseClicked(MouseEvent e) {
                showChart(new StartForm());
            }
        });

        btnStockMonitoring.addActionListener(btnListener);
        btnWebAnalytics.addActionListener(btnListener);
        btnSalesFigures.addActionListener(btnListener);
        btnStackedBars.addActionListener(btnListener);
        btnServerStatus.addActionListener(btnListener);
        btnVegetationGrowth.addActionListener(btnListener);
        btnSalesFiguresPie.addActionListener(btnListener);
        btnMultiplePies.addActionListener(btnListener);
        btnDonut.addActionListener(btnListener);
        btnMultiDonut.addActionListener(btnListener);
        btnProductShipments.addActionListener(btnListener);
        btnProjectPlanner.addActionListener(btnListener);
        btnPopularCodeLanguages.addActionListener(btnListener);
        btnScroller.addActionListener(btnListener);
    }

