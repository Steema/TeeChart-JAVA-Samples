    private void showChart(JPanel chart) {
        pnlChart.removeAll();

        pnlSample = chart;

        pnlChart.add(pnlSample);
        pnlChart.invalidate();
        pnlChart.validate();
        pnlChart.repaint();
    }

    private void expandCollapse(JPanel pnlMenu, JButton btnGroup, int nSamples) {
        int totalHeight = btnGroup.getHeight() + btnStockMonitoring.getHeight() * nSamples;
        if (pnlMenu.getHeight() == btnGroup.getHeight() + 2) {
            pnlMenu.setSize(pnlMenu.getWidth(), totalHeight);
            btnGroup.setIcon(new ImageIcon(getClass().getResource("/Resources/up.png")));
        } else {
            pnlMenu.setSize(pnlMenu.getWidth(), btnGroup.getHeight() + 2);
            btnGroup.setIcon(new ImageIcon(getClass().getResource("/Resources/down.png")));
        }

        setButtonsLocations();
    }
    
    private void setButtonsLocations() {
        pnlMenuBars.setLocation(pnlMenuBars.getX(), pnlMenuLines.getHeight());
        pnlMenuAreas.setLocation(pnlMenuAreas.getX(), pnlMenuBars.getY() + pnlMenuBars.getHeight());
        pnlMenuPieDonut.setLocation(pnlMenuPieDonut.getX(), pnlMenuAreas.getY() + pnlMenuAreas.getHeight());
        pnlMenuPoints.setLocation(pnlMenuPoints.getX(), pnlMenuPieDonut.getY() + pnlMenuPieDonut.getHeight());
        pnlMenuGantt.setLocation(pnlMenuGantt.getX(), pnlMenuPoints.getY() + pnlMenuPoints.getHeight());
        pnlMenuBubbles.setLocation(pnlMenuBubbles.getX(), pnlMenuGantt.getY() + pnlMenuGantt.getHeight());
        pnlMenuMiscellaneous.setLocation(pnlMenuMiscellaneous.getX(), pnlMenuBubbles.getY() + pnlMenuBubbles.getHeight());
    }

